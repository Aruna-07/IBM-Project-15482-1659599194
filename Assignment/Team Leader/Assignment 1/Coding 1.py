

lista=[1,2,3,4,5]
print(lista)

#insertion
lista.insert(5,10)
print(lista)

#deletion of first occurence of integer
del lista[0]
print(lista)

#insertion at end of lista
lista.append(-5)
print(lista)

#sorting
lista.sort()
print(lista)

#popping last element
lista.pop()
print(lista)

#reverse the lista
lista.reverse()
print(lista)

