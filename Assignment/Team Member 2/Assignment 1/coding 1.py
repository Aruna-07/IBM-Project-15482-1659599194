

listx=[1,2,3,4,5]
print(listx)

#insertion
listx.insert(5,10)
print(listx)

#deletion of first occurence of integer
del listx[0]
print(listx)

#insertion at end of listx
listx.append(-5)
print(listx)

#sorting
listx.sort()
print(listx)

#popping last element
listx.pop()
print(listx)

#reverse the listx
listx.reverse()
print(listx)

